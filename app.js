/* ============================= PAGE: NEWS ============================= */
function photoSlotEmpty(label){
  return '<div class="photo-slot"><span data-icon="camera" data-icon-size="22"></span>'+label+' coming soon</div>';
}
function photoGrid(list, label){
  const cells = list.length ? list.map(function(src){ return '<div class="photo-slot"><img src="'+src+'" alt="'+label+'"></div>'; })
                            : [photoSlotEmpty(label), photoSlotEmpty(label), photoSlotEmpty(label)];
  return '<div class="photo-grid">'+cells.join('')+'</div>';
}

