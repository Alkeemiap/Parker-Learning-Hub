/* ---------------- helpers ---------------- */
function listHTML(items){ return items.map(function(t){ return '<li>'+t+'</li>'; }).join(''); }
function checkListHTML(items){
  const tick = '<svg width="11" height="11" viewBox="0 0 24 24" fill="none" stroke="currentColor" ' +
               'stroke-width="2.4" stroke-linecap="round" stroke-linejoin="round"><path d="M5 13l4 4 10-10"/></svg>';
  return '<ul class="check-list">' + items.map(function(t){
    return '<li><span class="tick">'+tick+'</span><span>'+t+'</span></li>';
  }).join('') + '</ul>';
}
function plainListHTML(items){ return '<ul class="plain-list">' + listHTML(items) + '</ul>'; }

