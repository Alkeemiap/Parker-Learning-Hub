function icon(name, size){
  size = size || 20;
  const body = ICONS[name] || ICONS.star;
  return '<svg class="icon" width="'+size+'" height="'+size+'" viewBox="0 0 24 24" fill="none" ' +
         'stroke="currentColor" stroke-width="1.8" stroke-linecap="round" stroke-linejoin="round">'+body+'</svg>';
}
function iconize(root){
  (root||document).querySelectorAll('[data-icon]').forEach(function(el){
    const size = el.getAttribute('data-icon-size') || 20;
    el.innerHTML = icon(el.getAttribute('data-icon'), size);
  });
}

/* ---------------- Nav + footer (shared across all pages) ---------------- */
const NAV_ITEMS = [
  { page: 'brief',  href: 'index.html',  label: 'Instructional Brief', icon: 'clipboard' },
  { page: 'family', href: 'family.html', label: 'Family Connection',   icon: 'heart' },
  { page: 'news',   href: 'news.html',   label: 'Classroom News',      icon: 'megaphone' }
];

function renderChrome(){
  const current = document.body.getAttribute('data-page');

  const navHtml = NAV_ITEMS.map(function(item){
    const cls = item.page === current ? 'active' : '';
    return '<a href="'+item.href+'" class="'+cls+'"><span data-icon="'+item.icon+'" data-icon-size="16"></span>'+item.label+'</a>';
  }).join('');

  document.querySelectorAll('[data-slot="nav"]').forEach(function(el){
    el.innerHTML =
      '<div class="nav-inner">' +
        '<div class="brand"><div class="mark">P</div>' +
          '<div class="txt"><div class="name">The Parker Learning Hub</div>' +
          '<div class="sub">'+CONTENT.meta.school+'</div></div></div>' +
        '<nav class="links" id="navLinks">'+navHtml+'</nav>' +
        '<button class="nav-toggle" id="navToggle" aria-label="Open menu"><span data-icon="menu" data-icon-size="24"></span></button>' +
      '</div>';
  });

  document.querySelectorAll('[data-slot="footer"]').forEach(function(el){
    el.innerHTML =
      '<div class="container">' +
        '<div class="vision-title">'+CONTENT.meta.teacherName+'\u2019s Classroom Vision</div>' +
        '<p>Every lesson is intentionally designed to build strong readers, critical thinkers, confident writers, ' +
        'and collaborative learners. Through rigorous instruction, meaningful discussion, and evidence-based thinking, ' +
        'students develop the knowledge and skills necessary for success in school and beyond.</p>' +
        '<div class="foot-links">' + NAV_ITEMS.map(function(i){ return '<a href="'+i.href+'">'+i.label+'</a>'; }).join('') +
        '<a href="mailto:'+ (CONTENT.family && CONTENT.family.resources && CONTENT.family.resources[0] ? CONTENT.family.resources[0].href.replace('mailto:','') : 'parker@example.edu') +'">Email '+CONTENT.meta.teacherName+'</a></div>' +
        '<div class="foot-bottom">'+CONTENT.meta.school+' \u00B7 '+CONTENT.meta.gradeSubject+'</div>' +
      '</div>';
  });

  iconize(document);

  const toggle = document.getElementById('navToggle');
  const links = document.getElementById('navLinks');
  if(toggle && links){
    toggle.addEventListener('click', function(){
      links.classList.toggle('open');
      toggle.innerHTML = icon(links.classList.contains('open') ? 'close' : 'menu', 24);
    });
  }
}

